    if [[ -f "$BACKEND_DIR/phpunit.xml" ]] || [[ -f "$BACKEND_DIR/phpunit.xml.dist" ]]; then
        echo -e "${BOLD}PHPUnit Tests${RESET}"
        cd "$BACKEND_DIR"
        if php artisan test --compact --no-interaction 2>/dev/null; then
            echo -e "${GREEN}✓ PHP tests passed${RESET}\n"
        else
            echo -e "${RED}✗ PHP tests failed${RESET}\n"
            exit_code=1
        fi
    fi

    # Python tests
    if [[ -f "$AI_GATEWAY_DIR/pytest.ini" ]] || [[ -f "$AI_GATEWAY_DIR/pyproject.toml" ]]; then
        echo -e "${BOLD}Pytest Suite${RESET}"
        cd "$AI_GATEWAY_DIR"
        if python -m pytest --asyncio-mode=auto -v --tb=short 2>/dev/null; then
            echo -e "${GREEN}✓ Python tests passed${RESET}\n"
        else
            echo -e "${RED}✗ Python tests failed${RESET}\n"
            exit_code=1
        fi
    fi

    # Cypress E2E tests
    if [[ -f "$FRONTEND_DIR/cypress.config.js" ]] && command -v npx &>/dev/null; then
        echo -e "${BOLD}Cypress E2E Tests${RESET}"
        cd "$FRONTEND_DIR"
        if npx cypress run --e2e 2>/dev/null; then
            echo -e "${GREEN}✓ E2E tests passed${RESET}\n"
        else
            echo -e "${RED}✗ E2E tests failed${RESET}\n"
            exit_code=1
        fi
    fi

    # k6 smoke test
    if command -v k6 &>/dev/null; then
        echo -e "${BOLD}k6 Smoke Test${RESET}"
        if k6 run "$PROJECT_DIR/k6/scripts/smoke.js" 2>/dev/null; then
            echo -e "${GREEN}✓ k6 smoke test passed${RESET}\n"
        else
            echo -e "${YELLOW}⚠ k6 smoke test completed with issues${RESET}\n"
        fi
    fi

    echo -e "${BOLD}${CYAN}══════ Test suite complete ══════${RESET}"
    return $exit_code
}

clean() {
    echo -e "${RED}Cleaning Corex.dev development environment...${RESET}\n"

    # Confirm unless --force
    if [[ "${1:-}" != "--force" ]]; then
        echo -e "${YELLOW}This will remove ALL Docker volumes, caches, and build artifacts.${RESET}"
        echo -n "Are you sure? [y/N] "
        read -r confirm
        if [[ "$confirm" != "y" && "$confirm" != "Y" ]]; then
            echo "Cancelled"
            return 1
        fi
    fi

    # Stop and remove containers + volumes
    echo "Stopping services..."
    $DOCKER_COMPOSE -f "$COMPOSE_FILE" down -v --remove-orphans 2>/dev/null || true
    if [[ -f "$MONITORING_COMPOSE" ]]; then
        $DOCKER_COMPOSE_MONITORING down -v --remove-orphans 2>/dev/null || true
    fi

    # Remove caches and build artifacts
    echo "Removing caches..."
    rm -rf "$BACKEND_DIR/storage/framework/cache"/*
    rm -rf "$BACKEND_DIR/storage/framework/views"/*
    rm -rf "$BACKEND_DIR/storage/framework/sessions"/*
    rm -rf "$BACKEND_DIR/storage/logs"/*
    rm -rf "$BACKEND_DIR/bootstrap/cache"/*
    rm -rf "$BACKEND_DIR/vendor" 2>/dev/null || true
    rm -rf "$BACKEND_DIR/node_modules" 2>/dev/null || true
    rm -rf "$BACKEND_DIR/public/build"/* 2>/dev/null || true
    rm -rf "$AI_GATEWAY_DIR/.venv" 2>/dev/null || true
    rm -rf "$AI_GATEWAY_DIR/__pycache__" 2>/dev/null || true
    rm -rf "$AI_GATEWAY_DIR/**/__pycache__" 2>/dev/null || true

    # Monitoring data
    if [[ -d "$MONITORING_DIR" ]]; then
        rm -rf "$MONITORING_DIR/grafana/data"/* 2>/dev/null || true
        rm -rf "$MONITORING_DIR/prometheus/data"/* 2>/dev/null || true
        rm -rf "$MONITORING_DIR/loki/data"/* 2>/dev/null || true
        rm -rf "$MONITORING_DIR/tempo/data"/* 2>/dev/null || true
    fi

    # SSL certs (recreate on next init)
    rm -f "$PROJECT_DIR/docker/nginx/ssl/"*.pem 2>/dev/null || true

    # Remove Docker images (optional)
    echo ""
    echo -e "${YELLOW}Remove built Docker images too?${RESET}"
    echo -n "This requires rebuilding. [y/N] "
    read -r confirm_images
    if [[ "$confirm_images" == "y" || "$confirm_images" == "Y" ]]; then
        $DOCKER_COMPOSE -f "$COMPOSE_FILE" down --rmi all 2>/dev/null || true
    fi

    echo ""
    echo -e "${GREEN}Clean complete. Run './scripts/init-dev.sh' to start fresh.${RESET}"
}

