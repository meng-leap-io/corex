main() {
    log_banner "Corex.dev — Development Environment Setup"

    CI_MODE=false
    RESET_MODE=false
    QUICK_MODE=false

    for arg in "$@"; do
        case "$arg" in
            --ci)   CI_MODE=true ;;
            --reset) RESET_MODE=true ;;
            --quick) QUICK_MODE=true ;;
            --help|-h)
                echo "Usage: $0 [OPTIONS]"
                echo ""
                echo "Options:"
                echo "  --quick   Skip image builds and seeds, just start services"
                echo "  --reset   Destroy all volumes and start fresh"
                echo "  --ci      Non-interactive CI mode (no sudo, migrate:fresh)"
                echo "  --help    Show this help"
                echo ""
                echo "Source this script to use helper functions:"
                echo "  source $0"
                echo "  start-dev  |  stop-dev  |  logs  |  test  |  clean"
                exit 0
                ;;
        esac
    done

    if [[ "$QUICK_MODE" == "true" ]]; then
        log_warn "Quick mode: skipping builds, seeds, and full checks"
        start_services
        health_check
        display_info
        exit 0
    fi

    check_docker
    check_docker_compose
    create_env_file
    generate_ssl_certs
    generate_laravel_key

    if [[ "$CI_MODE" == "false" ]]; then
        install_composer
        install_npm
    else
        log_info "CI mode: skipping dependency installation"
    fi

    setup_redis
    create_directories
    set_permissions

    if [[ "$RESET_MODE" == "true" ]]; then
        log_warn "Reset mode: destroying existing volumes"
        $DOCKER_COMPOSE -f "$COMPOSE_FILE" down -v --remove-orphans 2>/dev/null || true
    fi

    build_images
    start_services
    run_migrations
    seed_database
    health_check
    display_info
}

