RESET='\033[0m'
BOLD='\033[1m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
RED='\033[0;31m'
CYAN='\033[0;36m'
MAGENTA='\033[0;35m'

log_info()  { echo -e "${GREEN}[INFO]${RESET}  $*"; }
log_warn()  { echo -e "${YELLOW}[WARN]${RESET}  $*"; }
log_error() { echo -e "${RED}[ERROR]${RESET} $*"; }
log_step()  { echo -e "\n${BOLD}${CYAN}══ $* ══${RESET}\n"; }
log_banner() {
    echo -e "${CYAN}"
    echo "╔══════════════════════════════════════════════════════════════╗"
    printf "║  %-60s  ║\n" "$*"
    echo "╚══════════════════════════════════════════════════════════════╝"
    echo -e "${RESET}"
}

# ═══════════════════════════════════════════════════════════════════════════
#  STEP 1 — Docker & Docker Compose Check
# ═══════════════════════════════════════════════════════════════════════════

check_docker() {
    log_step "1/15 — Checking Docker installation"

    if ! command -v docker &>/dev/null; then
        log_error "Docker is not installed."
        echo "  Install Docker: https://docs.docker.com/engine/install/"
        echo "  Or use the quick-install script:"
        echo "    curl -fsSL https://get.docker.com | sh"
        exit 1
    fi

    DOCKER_VERSION=$(docker --version 2>/dev/null | grep -oP '\d+\.\d+\.\d+' | head -1 || true)
    log_info "Docker: $DOCKER_VERSION"

    if ! docker info &>/dev/null; then
        log_error "Docker daemon is not running or current user lacks permissions."
        echo "  Try: sudo usermod -aG docker $USER && newgrp docker"
        exit 1
    fi

    log_info "Docker daemon is running"
}

check_docker_compose() {
    log_step "2/15 — Checking Docker Compose"

    if docker compose version &>/dev/null; then
        COMPOSE_VERSION=$(docker compose version 2>/dev/null | grep -oP '\d+\.\d+\.\d+' | head -1 || true)
        log_info "Docker Compose (plugin): $COMPOSE_VERSION"
    elif docker-compose --version &>/dev/null; then
        DOCKER_COMPOSE="docker-compose"
        DOCKER_COMPOSE_MONITORING="docker-compose -f $MONITORING_COMPOSE -p corex-monitoring"
        log_warn "Using docker-compose (deprecated, upgrade to plugin)"
    else
        log_error "Docker Compose is not installed."
        echo "  Install: sudo apt-get install docker-compose-plugin"
        echo "  Or:      https://docs.docker.com/compose/install/"
        exit 1
    fi
}

