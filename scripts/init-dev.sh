    echo -e "  ${MAGENTA}start-dev${RESET}  ${MAGENTA}stop-dev${RESET}  ${MAGENTA}logs${RESET}  ${MAGENTA}test${RESET}  ${MAGENTA}clean${RESET}"
    echo ""
else
    main "$@"
fi
