# ═══════════════════════════════════════════════════════════════════════════
#  STEP 4 — Self-Signed SSL Certificates
# ═══════════════════════════════════════════════════════════════════════════

generate_ssl_certs() {
    log_step "4/15 — Generating self-signed SSL certificates"

    local SSL_DIR="$PROJECT_DIR/docker/nginx/ssl"
    mkdir -p "$SSL_DIR"

    if [[ -f "$SSL_DIR/fullchain.pem" && -f "$SSL_DIR/privkey.pem" ]]; then
        log_info "SSL certificates already exist at $SSL_DIR"
        return
    fi

    # Generate CA key and cert
    openssl req -x509 -nodes -new -sha256 \
        -days 3650 \
        -newkey rsa:2048 \
        -keyout "$SSL_DIR/ca-key.pem" \
        -out "$SSL_DIR/ca.pem" \
        -subj "/C=US/ST=California/L=San Francisco/O=Corex Dev/CN=Corex Dev CA" \
        2>/dev/null

    # Generate wildcard cert for *.corex.dev using the CA
    cat > "$SSL_DIR/domains.ext" <<EOF
authorityKeyIdentifier=keyid,issuer
basicConstraints=CA:FALSE
keyUsage=digitalSignature,keyEncipherment
extendedKeyUsage=serverAuth,clientAuth
subjectAltName=@alt_names

[alt_names]
DNS.1=corex.dev
DNS.2=*.corex.dev
DNS.3=localhost
DNS.4=*.localhost
DNS.5=api.corex.dev
DNS.6=console.corex.dev
EOF

    openssl req -new -nodes -newkey rsa:2048 \
        -keyout "$SSL_DIR/privkey.pem" \
        -out "$SSL_DIR/csr.pem" \
        -subj "/C=US/ST=California/L=San Francisco/O=Corex Dev/CN=corex.dev" \
        2>/dev/null

    openssl x509 -req -sha256 -days 3650 \
        -in "$SSL_DIR/csr.pem" \
        -CA "$SSL_DIR/ca.pem" \
        -CAkey "$SSL_DIR/ca-key.pem" \
        -CAcreateserial \
        -out "$SSL_DIR/fullchain.pem" \
        -extfile "$SSL_DIR/domains.ext" \
        2>/dev/null

    # Copy for nginx
    cp "$SSL_DIR/fullchain.pem" "$SSL_DIR/cert.pem"
    chmod 644 "$SSL_DIR/"*.pem
    chmod 600 "$SSL_DIR/privkey.pem"
    rm -f "$SSL_DIR/domains.ext" "$SSL_DIR/csr.pem" "$SSL_DIR/ca-key.pem" "$SSL_DIR/ca.pem" "$SSL_DIR/ca.srl"

    log_info "Self-signed certificates created at $SSL_DIR"
    log_warn "Add CA to your trust store to avoid browser warnings:"
    echo "  sudo security add-trusted-cert -d -r trustRoot -k /Library/Keychains/System.keychain $SSL_DIR/ca.pem (macOS)"
    echo "  sudo cp $SSL_DIR/ca.pem /usr/local/share/ca-certificates/corex-ca.crt && sudo update-ca-certificates (Linux)"
}

# ═══════════════════════════════════════════════════════════════════════════
#  STEP 5 — Laravel Key Generation
# ═══════════════════════════════════════════════════════════════════════════

generate_laravel_key() {
    log_step "5/15 — Generating Laravel application key"

    cd "$BACKEND_DIR"

    if ! grep -q "^APP_KEY=base64:" ".env" 2>/dev/null; then
        php artisan key:generate --force --no-interaction 2>/dev/null || {
            log_warn "PHP not available locally — generating APP_KEY manually"
            local key
            key=$(openssl rand -base64 32)
            sed -i "s|^APP_KEY=.*|APP_KEY=base64:${key}|" "$ENV_FILE"
        }
        log_info "APP_KEY generated"
    else
        log_info "APP_KEY already set"
    fi
}

# ═══════════════════════════════════════════════════════════════════════════
#  STEP 6 — Composer Install
# ═══════════════════════════════════════════════════════════════════════════

install_composer() {
    log_step "6/15 — Installing Composer dependencies"

    cd "$BACKEND_DIR"

    if command -v composer &>/dev/null; then
        composer install --no-interaction --prefer-dist --optimize-autoloader 2>&1 | tail -3
        log_info "Composer dependencies installed"
    elif command -v docker &>/dev/null; then
        log_info "Installing via Docker (composer image)..."
        docker run --rm \
            -v "$BACKEND_DIR:/app" \
            -v "${COMPOSER_HOME:-$HOME/.composer}:/tmp/cache" \
            composer:latest \
            install --no-interaction --prefer-dist --optimize-autoloader 2>&1 | tail -3
        log_info "Composer dependencies installed via Docker"
    else
        log_warn "Skipping Composer install — no PHP or Docker available"
    fi
}

# ═══════════════════════════════════════════════════════════════════════════
#  STEP 7 — NPM Install
# ═══════════════════════════════════════════════════════════════════════════

install_npm() {
    log_step "7/15 — Installing NPM dependencies"

    if [[ ! -f "$BACKEND_DIR/package.json" ]]; then
        log_info "No package.json found in backend — skipping"
        return
    fi

    if command -v npm &>/dev/null; then
        cd "$BACKEND_DIR"
        npm install 2>&1 | tail -3
        log_info "NPM dependencies installed"
    elif command -v docker &>/dev/null; then
        log_info "Installing via Docker (node image)..."
        docker run --rm \
            -v "$BACKEND_DIR:/app" \
            -w /app \
            node:20-alpine \
            npm install 2>&1 | tail -3
        log_info "NPM dependencies installed via Docker"
    else
        log_warn "Skipping NPM install — no Node or Docker available"
    fi
}

