# ═══════════════════════════════════════════════════════════════════════════
#  HELPER FUNCTIONS (for sourcing)
# ═══════════════════════════════════════════════════════════════════════════

start-dev() {
    echo -e "${CYAN}Starting Corex.dev services...${RESET}"
    $DOCKER_COMPOSE -f "$COMPOSE_FILE" up -d --remove-orphans
    if [[ -f "$MONITORING_COMPOSE" ]]; then
        $DOCKER_COMPOSE_MONITORING up -d --remove-orphans
    fi
    health_check
}

stop-dev() {
    echo -e "${YELLOW}Stopping Corex.dev services...${RESET}"
    $DOCKER_COMPOSE -f "$COMPOSE_FILE" down --remove-orphans
    if [[ -f "$MONITORING_COMPOSE" ]]; then
        $DOCKER_COMPOSE_MONITORING down --remove-orphans
    fi
    echo -e "${GREEN}All services stopped${RESET}"
}

logs() {
    local follow="${1:--f}"
    local services=("nginx" "php" "ai-gateway" "postgres" "redis" "queue")

    echo -e "${CYAN}Tailing logs for core services...${RESET}"
    $DOCKER_COMPOSE -f "$COMPOSE_FILE" logs "$follow" "${services[@]}"
}

test() {
    echo -e "${BOLD}${CYAN}══════ Running Corex.dev Test Suite ══════${RESET}\n"

    local exit_code=0

