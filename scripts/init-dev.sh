# ═══════════════════════════════════════════════════════════════════════════
#  STEP 10 — Redis Setup
# ═══════════════════════════════════════════════════════════════════════════

setup_redis() {
    log_step "10/15 — Setting up Redis"

    if command -v redis-cli &>/dev/null; then
        if redis-cli ping 2>/dev/null | grep -q PONG; then
            log_info "Redis is running locally"
        else
            log_warn "Redis not available locally — will be available via Docker"
        fi
    else
        log_warn "redis-cli not installed locally — will be available via Docker"
    fi

    # Update .env for Docker-based Redis
    sed -i 's|^REDIS_HOST=.*|REDIS_HOST=redis|' "$ENV_FILE"
    sed -i 's|^REDIS_PASSWORD=.*|REDIS_PASSWORD=null|' "$ENV_FILE"

    log_info "Redis configuration updated in .env"
}

