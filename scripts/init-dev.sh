# ═══════════════════════════════════════════════════════════════════════════
#  STEP 12 — Set Permissions
# ═══════════════════════════════════════════════════════════════════════════

set_permissions() {
    log_step "12/15 — Setting permissions"

    # Storage: writable by web server (www-data:33) and current user
    chmod -R 775 "$BACKEND_DIR/storage"
    chmod -R 775 "$BACKEND_DIR/bootstrap/cache"

    # Try to set proper ownership
    if [[ "$(uname -s)" == "Linux" ]]; then
        if command -v id &>/dev/null; then
            chown -R "$(id -u):$(id -g)" "$BACKEND_DIR/storage" 2>/dev/null || true
            chown -R "$(id -u):$(id -g)" "$BACKEND_DIR/bootstrap/cache" 2>/dev/null || true
        fi
        # Also allow www-data group for Docker
        chmod -R g+ws "$BACKEND_DIR/storage" 2>/dev/null || true
        chmod -R g+ws "$BACKEND_DIR/bootstrap/cache" 2>/dev/null || true
    fi

    # SSL certs
    chmod 600 "$PROJECT_DIR/docker/nginx/ssl/privkey.pem" 2>/dev/null || true
    chmod 644 "$PROJECT_DIR/docker/nginx/ssl/"*.pem 2>/dev/null || true

    # Scripts
    chmod +x "$PROJECT_DIR/scripts/"*.sh

    log_info "Permissions set"
}

