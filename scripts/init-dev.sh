# ═══════════════════════════════════════════════════════════════════════════
#  STEP 15 — Health Check
# ═══════════════════════════════════════════════════════════════════════════

health_check() {
    log_step "15/15 — Checking service health"

    local all_healthy=true
    local max_retries=30
    local retry_interval=3

    echo ""
    printf "%-30s %s\n" "SERVICE" "STATUS"
    printf "%-30s %s\n" "-------" "------"

    check_service() {
        local name=$1
        local container=$2
        local url=$3
        local retries=${4:-$max_retries}

        for ((i = 0; i < retries; i++)); do
            local status
            status=$($DOCKER_COMPOSE -f "$COMPOSE_FILE" ps "$container" --format '{{.Status}}' 2>/dev/null || echo "")

            if echo "$status" | grep -qE "(healthy|running|up)"; then
                if [[ -n "$url" ]]; then
                    if curl -sf "$url" &>/dev/null; then
                        printf "%-30s ${GREEN}✓${RESET} healthy\n" "$name"
                        return 0
                    fi
                else
                    printf "%-30s ${GREEN}✓${RESET} running\n" "$name"
                    return 0
                fi
            fi
            sleep "$retry_interval"
        done

        printf "%-30s ${RED}✗${RESET} unhealthy\n" "$name"
        all_healthy=false
        return 1
    }

    check_service "PostgreSQL" "postgres" "" 15
    check_service "Redis" "redis" "redis-cli -h localhost ping" 10
    check_service "PHP-FPM" "php" "" 15
    check_service "AI Gateway" "ai-gateway" "http://localhost:8000/health" 15
    check_service "Nginx" "nginx" "http://localhost:80/health" 15
    check_service "Queue Worker" "queue" "" 10
    check_service "Scheduler" "scheduler" "" 10

    if [[ -f "$MONITORING_COMPOSE" ]]; then
        check_service "Prometheus" "prometheus" "http://localhost:9090/-/ready" 10 2>/dev/null || true
        check_service "Grafana" "grafana" "http://localhost:3000/api/health" 10 2>/dev/null || true
    fi

    echo ""

    if [[ "$all_healthy" == "true" ]]; then
        log_info "All services are healthy!"
    else
        log_warn "Some services are not healthy. Check logs with: $0 logs"
    fi
}

